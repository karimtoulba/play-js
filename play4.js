// For Loop - FORWARD
for (let counter = 5; counter <= 10; counter++) {
  console.log(counter);
}

// For Loop - BACKGROUND
for (let counter = 5; counter >= 0; counter--) {
  console.log(counter);
}

// For Loop - ARRAY
let myCities = ['Amsterdam', 'Berlin', 'Oslo'];
for (let i = 0; i < myCities.length; i++) {
  console.log(myCities[i]);
}
